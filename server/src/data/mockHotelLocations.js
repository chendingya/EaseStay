/**
 * 按城市关键词过滤 mock 数据
 * @param {string} city
 */
function getMockByCity(city) {
  const keyword = city.replace(/市$/, '')
  return mockHotelLocations.filter(
    h => h.city.includes(keyword) || keyword.includes(h.city.replace(/市$/, ''))
  )
}

/**
 * 按城市返回地址坐标池（用于给 DB 酒店分配真实坐标+地址）
 * @param {string} city
 * @returns {{ address: string, lat: number, lng: number }[]}
 */
function getMockAddressPool(city) {
  return getMockByCity(city).map(h => ({
    address: h.address,
    lat: h.lat,
    lng: h.lng,
  }))
}

